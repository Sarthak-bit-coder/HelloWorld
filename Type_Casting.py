# Type casting = converting one data type to another data type
############################################################################################################################

x = 1  # int
y = 2.0  # float
z = "3"  # str

# Convert float to int
y = int(y)

# Convert str to int
z = int(z)

# convert int to str
x = float(x)

print(x)
print(y)
print(z * 3)
